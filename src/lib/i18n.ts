export function t(
  key: string,
  params?: Record<string, string | number>,
): string {
  const template = nl[key] ?? key;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, name) => {
    const value = params[name];
    return value === undefined ? `{${name}}` : String(value);
  });
}
